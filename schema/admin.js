const mongoose = require('mongoose');

const Schema = mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const AdminSchema = mongoose.model('admin', Schema);
module.exports = AdminSchema;